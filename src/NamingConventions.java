
public class NamingConventions {

	public static void main(String[] args) 
	{
		/*
		 * Interface : Adjective
		 * e.g Runable, Readable, Remote // 1st letter capital
		 * 
		 * Class : Noun
		 * e.g Student, Person, Animal, Place, Thing // 1st letter Capital
		 * 
		 * Method : Verb
		 * e.g actionPerformed, run, display, eating etc // 1st letter is not capital
		 * 
		 * Variable : Attribute // 1st letter small
		 * e.g stockprice, bankdetails, sname, speed
		 * 
		 * Constant:
		 * e.g PI, DENSITY, MAX_SPEED // all letters capital.
		 * 
		 * Package name: 1st letter should be small.
		 * 
		 * Java uses camel casing rule so if e.g your class name has 2 words MyName 2nd letter should also 
		 * capital. it applies to all e.g methods classes variables interfaces except packages
		 */
	}

}
