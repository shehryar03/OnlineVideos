public class Iteration {
    public static void main(String[] args)
    {
     /// for loop used for fixed iterations
    /// FOR LOOP ///
    for (int a=0;a<6;a++)
    {
    	System.out.println("Ich liebe dich");
    }
    
   /// WHILE LOOP ///
   int x = 0;
   while(x<=7)
   {
	   System.out.println("Mehr");
	   x++;
   }
   
   //// DO WHILE ///
   int y = 0;
   do
   {
	   System.out.println("WHILE LOOP");
	   y++;
   } while(y<=6);
}
}